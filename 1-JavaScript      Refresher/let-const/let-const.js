// Let-Const-Var explaination:

// var: older way of making variable:
// can be redeclared again.
// exmple:
var name = "Shivam";
console.log(name);

// const:to declare a variable with constant value:
// exmple:
const PI = 3.14;
console.log(PI);
PI = 3.24;
console.log(PI); //will show error

// let: new way of variable declaration:
// need not to declare the same variable again and again.
// exmple:
let age = 20;
console.log(age);
age = 21;
console.log(age);
